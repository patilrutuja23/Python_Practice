# bitwise AND(&).
print("AND")
a = 12
b = 13
c = a&b
print(a)
print(b)
print(c)

# bitwise OR(|)
print("OR")
a = 12
b = 13
c = a|b
print(a)
print(b)
print(c)

# bitwise X-OR(^)
print("X-OR")
a = 12
b = 13
c = a^b
print(a)
print(b)
print(c)

# Left shift(<<)
print("left shift")
a = 4
b = 2
c = a<<b
print(a)
print(b)
print(c)
#left shift by 1 gives 2a
#left shift by 2 gives 4a
# a*2^b


# Right shift(<<)
print("Right shift")
a = 4
b = 1
c = a>>b
print(a)
print(b)
print(c)
#Right shift by 1 gives a/2
# a/2^b

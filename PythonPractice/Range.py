# range(start(from 0) to passing number)
l = list(range(10))
print(l)

# range(start, end)
l1 = list(range(1,11))
print(l1)

# range(start, end, step/gap)
l2 = tuple(range(2,11,2))
print(l2)

# for binary number
print(bin(10))

# for octadecimal number
print(oct(10))


# for hexadecimal number
print(hex(10))